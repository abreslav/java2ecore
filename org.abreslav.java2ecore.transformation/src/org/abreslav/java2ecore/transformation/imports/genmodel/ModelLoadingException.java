package org.abreslav.java2ecore.transformation.imports.genmodel;

public class ModelLoadingException extends Exception {

	private static final long serialVersionUID = -4207236829907819583L;

	public ModelLoadingException() {
		super();
	}

	public ModelLoadingException(String message, Throwable cause) {
		super(message, cause);
	}

	public ModelLoadingException(String message) {
		super(message);
	}

	public ModelLoadingException(Throwable cause) {
		super(cause);
	}
}
